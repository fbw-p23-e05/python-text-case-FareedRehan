# Upper / lower in Python

## Tasks

### Original string
text = "The Quick BroWnFoX jumps over the laZy Dog!"
print("Original string:", text)

### uppercase string
text = "The Quick BroWnFoX jumps over the laZy Dog!"
print("String in uppercase:", text.upper())

## lowercase string
text = "The Quick BroWnFoX jumps over the laZy Dog!"
print("String in lowercase:", text.lower())
